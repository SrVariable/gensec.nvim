---@module "luassert"
local gensec = require("gensec")

local section_cases = {
	["/*"] = "/* @------------------------------------------------------------------------@ */\
/* |                              Test Section                              | */\
/* @------------------------------------------------------------------------@ */",
	["#"] = "# @--------------------------------------------------------------------------@ #\
# |                               Test Section                               | #\
# @--------------------------------------------------------------------------@ #",
	["--"] = "-- @------------------------------------------------------------------------@ --\
-- |                              Test Section                              | --\
-- @------------------------------------------------------------------------@ --",
	['"'] = '" @--------------------------------------------------------------------------@ "\
" |                               Test Section                               | "\
" @--------------------------------------------------------------------------@ "',
}

describe("gensec", function()
	local title = "Test"
	for comment_type, expected in pairs(section_cases) do
		it("should create a " .. title .. " Section using '" .. comment_type .. "'", function()
			local result = gensec.generate_section(title, comment_type)
			assert.is_true(result == expected)
		end)
	end
end)
